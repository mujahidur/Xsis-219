package com.xsis.day02.math;

public class Practice01 {
    public static void main(String[] args) {
        // math abs, akan return number absolut
        int n = -4;
        double m = 4.5;

        System.out.println("Math absolute: " + Math.abs(n));
        System.out.println("Math double: " + Math.abs(m));

        // math pow untuk calculate eksponensial number
        System.out.println("Math pow: " + Math.pow(2,3));

        // math sqrt (akar)
        System.out.println("Math sqrt: " + Math.sqrt(4));

        // math randomize
        System.out.println("Math randomize: " + Math.random());
    }
}
