package com.xsis.day02.string;

public class String01 {
    public static void main(String[] args) {
        String hello = "Hello kamu yang disana";
        System.out.println(hello);
        System.out.println("index ke-0: " + hello.charAt(0));
        System.out.println("index ke-3: " + hello.charAt(3));
        System.out.println("index ke-7: " + hello.charAt(7));
        System.out.println("index ke-13: " + hello.charAt(13));
    }
}
